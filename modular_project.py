# YOUR FUNCTION HERE
def knots_to_kmh(knots):
    return knots * 1.852

def feet_to_cm(feet):
    return feet * 30.48

def inches_to_cm(inches):
    return inches * 2.54

def cups_to_grams(cups):
    return cups * 128
